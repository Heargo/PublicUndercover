    <h2>Explication d'un tour de jeu</h2>
    <p>
        Au début de la partie vous aurez 2 informations : votre Rôle et votre mot secret !<br/>
        Les membres ont un mot secret, les Undercover un mot diférent mais similaire à celui des Membres<br/>
    </p>
