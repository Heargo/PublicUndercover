    <h2>Comment jouer ?</h2>

    <p>
        Avant de pouvoir jouer il vous faut un moyen de communication avec les autre joueurs (A coté dans la vrai vie, discord, skype...). <br/><br/>
        Il faut également créer un compte en cliquant sur le bouton 'SIGN UP' si vous n'avez pas déja un compte. Dans ce cas connectez vous<br/>
        avec le bouton 'LOG IN'.<br/><br/>
        Je vous invite ensuite à cliquer sur le bouton 'PLAY' puis choisir si vous voulez rejoindre ou créer une partie. <br/>
        Si vous voulez rejoindre une partie vous devrez demander à l'hôte l'id de cette dernière et le mot de passe (si il y en a un)<br/>
        Puis l'hôte peut choisir de lancer la partie et c'est parti ! <br/>
        Bonne chance et amusez vous bien !!
    </p>

